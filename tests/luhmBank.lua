local cardLengthArray = {14, 15, 16, 17, 18, 19}

function luhmLengthCheck(bankno)
  if bankno == nil then
    return false
  end
  
  for j = 1, #cardLengthArray do
    local length = cardLengthArray[j]
    if length == #bankno then
      return true
    end
  end
  
  return false
end

function luhmCheck(bankno)
  if isEncrypt(bankno) then
    return true
  end
  if bankno == nil then
    return false
  end
  
  if not luhmLengthCheck(bankno) then
    return false
  end
  
  if string.rep('0', #bankno) == bankno then
    return false
  end
  
  if string.rep('8', #bankno) == bankno then
    return false
  end

  local lastNum = string.sub(bankno, #bankno, #bankno) --取出最后一位（与luhm进行比较）

  local first15Num = string.sub(bankno, 1, #bankno - 1) --取出最后一位（与luhm进行比较）
  local newArr = {}
  
  for i = 1, #first15Num do 
    newArr[#first15Num - i + 1] = string.sub(first15Num, i, i)
  end
  
  local arrJiShu = {} --奇数位*2的积 <9
  local arrJiShu2 = {} --奇数位*2的积 >9

  local arrOuShu = {} --数位数组
  for j = 1, #newArr do 
    if ((j + 1) % 2 == 0) then --奇数位
      if (newArr[j] * 2 < 9) then
        arrJiShu[#arrJiShu + 1] = math.floor(newArr[j] * 2);
            else
        arrJiShu2[#arrJiShu2 + 1] = math.floor(newArr[j] * 2);
      end
        else --偶数位
      arrOuShu[#arrOuShu + 1] = math.floor(newArr[j]);
    end
    end
  
  local jishu_child1 = {} --奇数位*2 >9 的分割之后的数组个位数
  local jishu_child2 = {} --奇数位*2 >9 的分割之后的数组十位数
  for h = 1, #arrJiShu2 do
      jishu_child1[h] = math.floor(arrJiShu2[h] % 10);
      jishu_child2[h] = math.floor(arrJiShu2[h] / 10);
  end
  
  local sumJiShu = 0 --奇数位*2 < 9 的数组之和
  local sumOuShu = 0 --偶数位数组之和
  local sumJiShuChild1 = 0 --奇数位*2 >9 的分割之后的数组个位数之和
  local sumJiShuChild2 = 0 --奇数位*2 >9 的分割之后的数组十位数之和
  local sumTotal = 0
  for m = 1, #arrJiShu do
      sumJiShu = sumJiShu + arrJiShu[m]
  end

  for n = 1, #arrOuShu do
      sumOuShu = sumOuShu + arrOuShu[n]
  end

  for p = 1, #jishu_child1 do
      sumJiShuChild1 = sumJiShuChild1 + jishu_child1[p]
      sumJiShuChild2 = sumJiShuChild2 + jishu_child2[p]
  end
  --计算总和
  sumTotal = sumJiShu + sumOuShu + sumJiShuChild1 + sumJiShuChild2

  --计算Luhm值
  local k = 0
  if (sumTotal % 10 == 0) then
    k = 10
  else
    k = sumTotal % 10
  end
  
  local luhm = 10 - k

  if (tostring(lastNum) == tostring(luhm)) then
      return true
  else
      return false
  end
end

