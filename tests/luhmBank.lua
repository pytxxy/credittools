function isEncrypt(bankno)
  if bankno == nil then
    return false
  end
  
  if not luhmLengthCheck(bankno) then
    return false
  end
  
  local first6Text = string.sub(bankno, 1, 6)
  local last4Text = string.sub(bankno, #bankno - 3, #bankno)
  local middleText = string.sub(bankno, 7, #bankno - 4)
  local first6Num = tonumber(first6Text);
  local last4Num = tonumber(last4Text);
  local middleNum = tonumber(middleText);
  return first6Num ~= nil and last4Num ~= nil and middleNum == nil and string.rep(string.sub(middleText, 1, 1), #middleText) == middleText
end

