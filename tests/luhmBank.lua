local function getCardItemWithArray(bankno)
  --print(bankno)
  if not luhmCheck(bankno) then
    return nil
  end
  
  --print(bankno.." check passed!")
  for i = 1, #cardInfo do
    local prefix = cardInfo[i]["prefix"]
    local banknoPrefix = string.sub(bankno, 1, #prefix)
    if (prefix == banknoPrefix and #bankno == cardInfo[i]["length"]) then
      return cardInfo[i]
    end
  end
  
  return nil
end

local function getCardItemFromIndexArray(bankno, itemIndexs)
  for k, v in pairs(itemIndexs) do
    if (#bankno == cardInfo[v]["length"]) then
      local itemPrefix = cardInfo[v]["samediff"]
      if itemPrefix == nil then
        return cardInfo[v]
      else
        local prefixLen = #itemPrefix
        local cardnoPrefix = string.sub(bankno, 1, prefixLen)
        if cardnoPrefix == itemPrefix then
          return cardInfo[v]
        end
      end
    end
  end

  return nil
end

