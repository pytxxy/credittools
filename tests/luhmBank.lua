  --print(bankno)
  if not luhmCheck(bankno) then
    return nil
  end
  
  local prefixLens = {6, 5, 4, 3}
  local cardItem = nil
  for i = 1, #prefixLens do
    local prefixLen = prefixLens[i]
    local banknoPrefix = string.sub(bankno, 1, prefixLen)
    
    -- 使用数字为键的方式，速度没有显示提升，所以没有采用
--    local itemIndexs = cardInfoIndex[tonumber(banknoPrefix)]
    local itemIndexs = cardInfoIndex[banknoPrefix]
    if itemIndexs ~= nil then
      cardItem = getCardItemFromIndexArray(bankno, itemIndexs)
    else
      cardItem = nil
    end
    
    if cardItem ~= nil then
      break
    end
  end
    
  return cardItem
end

function getCardItem(bankno)
--  return getCardItemWithArray(bankno)
  return getCardItemWithMap(bankno)
end

function getCardInfo(bankno, key)
  local item = getCardItem(bankno)
  
  if item ~= nil then
    local rlt = nil
    local nameFlag = "name"
    if key == nameFlag then
      local index = item["index"]
      rlt = bankMap[index][nameFlag]
    elseif key == "type" then
      local code = item["code"]
      rlt = cardTypeMap[code]
    else
      rlt = item[key]
    end
    
    return rlt
  end
  
  return nil
end

function getLuhmValue(bankno, key)
  local item = getCardItem(bankno)
  
  if item ~= nil then
    --print("found "..prefix.."!")
    local index = item["index"]
    if (index > #bankMap or index < 1) then
      return nil
    end
    
    return bankMap[index][key]
  end
  
  return nil
end

local function getBankMapIndexWithName(name)
  for i = 1, #bankMap do
    local bankName = bankMap[i]["name"]
    if (bankName == name) then
      return i
    end
  end
  
  return nil
end

function getBankCodeWithName(name)
  local index = getBankMapIndexWithName(name)
  if index ~= nil then
    return bankMap[index]["code"]
  end
  
  return nil
end


